package com.example.parcial3_paredes_guerra_terrones_arango_duran.Entidades;


public class Nombre2 {

    private String Name;
    private String Like;
    private String Dislike;


    public Nombre2(String name, String like, String dislike){
        Name=name;
        Like=like;
        Dislike=dislike;
    }


    public String getName() {
        return Name;
    }

    public String getLike() {
        return Like;
    }

    public String getDislike() {
        return Dislike;
    }
}
